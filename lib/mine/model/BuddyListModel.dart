class BuddyListModel {
  int id;
  String mobile;
  String email;
  int status;
  String createdAt;

  BuddyListModel(
      {this.id, this.mobile, this.email, this.status, this.createdAt});

  BuddyListModel.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    mobile = json['mobile'];
    email = json['email'];
    status = json['status'];
    createdAt = json['created_at'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['id'] = this.id;
    data['mobile'] = this.mobile;
    data['email'] = this.email;
    data['status'] = this.status;
    data['created_at'] = this.createdAt;
    return data;
  }
}

